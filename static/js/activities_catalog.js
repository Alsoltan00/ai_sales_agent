function tagsFromName(name, mainSector, subSector) {
    const tokens = name
        .split(/[\s()/-]+/)
        .map(token => token.trim())
        .filter(token => token.length > 2);
    return [...new Set([subSector, ...tokens.slice(0, 3), mainSector])].slice(0, 5);
}

function buildActivitiesCatalog() {
    const rows = [];
    sectorBlueprint.forEach(sector => {
        let counter = 1;
        sector.subSectors.forEach(sub => {
            sub.activities.forEach(activityName => {
                rows.push({
                    id: `${sector.code}_${String(counter).padStart(3, '0')}`,
                    domain: sector.main,
                    main_sector: sector.main,
                    sub_sector: sub.name,
                    name: activityName,
                    description: '',
                    icon: sector.icon,
                    tags: tagsFromName(activityName, sector.main, sub.name),
                    rating: 5,
                    cost: ''
                });
                counter += 1;
            });
        });
    });
    return rows;
}

const activitiesCatalog = buildActivitiesCatalog();

// دالة البحث الذكي
function searchActivities(query) {
    if (!query) return activitiesCatalog;
    
    query = query.toLowerCase().trim();
    
    return activitiesCatalog.map(activity => {
        let score = 0;
        const name = activity.name.toLowerCase();
        const desc = activity.description.toLowerCase();
        const domain = activity.domain.toLowerCase();
        const mainSector = activity.main_sector.toLowerCase();
        const subSector = activity.sub_sector.toLowerCase();
        
        // 1. تطابق تام
        if (name === query) score += 100;
        
        // 2. يبدأ بـ
        else if (name.startsWith(query)) score += 50;
        
        // 3. يحتوي على
        else if (name.includes(query)) score += 30;
        
        // 4. تطابق في الكلمات المفتاحية (Tags)
        if (activity.tags.some(tag => tag.toLowerCase().includes(query))) score += 20;
        
        // 5. تطابق في المجال
        if (domain.includes(query)) score += 10;
        if (mainSector.includes(query)) score += 10;
        if (subSector.includes(query)) score += 15;
        
        // 6. تطابق في الوصف
        if (desc.includes(query)) score += 5;
        
        return { ...activity, score };
    })
    .filter(a => a.score > 0)
    .sort((a, b) => b.score - a.score);
}
